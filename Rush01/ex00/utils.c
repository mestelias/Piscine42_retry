#include "main.h"

int	is_digit(char c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}

int	is_separator(char c)
{
	if ((c >= 8 && c <= 13) || c == ' ')
		return (1);
	return (0);
}
