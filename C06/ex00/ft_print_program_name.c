#include <unistd.h>

void	ft_print_program_name(char *str)
{
	while (*str != '\0')
	{
		write(1, str, 1);
		str++;
	}
	write(1, "\n", 1);
}

int	main(int argc, char **argv)
{
	if (argc == 0)
		return (0);
	if (argv == 0)
		return (0);
	ft_print_program_name(argv[0]);
	return (0);
}
